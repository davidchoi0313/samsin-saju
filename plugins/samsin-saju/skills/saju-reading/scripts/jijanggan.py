# 지지 -> [여기, 중기(None 가능), 정기]
HIDDEN_STEMS = {
    "子": ["壬", None, "癸"],
    "丑": ["癸", "辛", "己"],
    "寅": ["戊", "丙", "甲"],
    "卯": ["甲", None, "乙"],
    "辰": ["乙", "癸", "戊"],
    "巳": ["戊", "庚", "丙"],
    "午": ["丙", "己", "丁"],
    "未": ["丁", "乙", "己"],
    "申": ["戊", "壬", "庚"],
    "酉": ["庚", None, "辛"],
    "戌": ["辛", "丁", "戊"],
    "亥": ["戊", "甲", "壬"],
}

_GAN_ELEMENT = {
    "甲": "木", "乙": "木", "丙": "火", "丁": "火", "戊": "土",
    "己": "土", "庚": "金", "辛": "金", "壬": "水", "癸": "水",
}

_ROLES = ("여기", "중기", "정기")


def hidden_stems(zhi):
    """지지 -> 지장간 리스트 [{"role","gan","element"}, ...] (여기->중기->정기 순).

    중기가 없는 지지(子卯酉)는 여기·정기 2개만 반환한다.
    결정론: 같은 입력 -> 항상 같은 출력.
    """
    out = []
    for role, gan in zip(_ROLES, HIDDEN_STEMS[zhi]):
        if gan is None:
            continue
        out.append({"role": role, "gan": gan, "element": _GAN_ELEMENT[gan]})
    return out
